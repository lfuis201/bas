    private void tabla1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla1MouseClicked
        int fila=tabla1.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        }else{
            id = Integer.parseInt(tabla1.getValueAt(fila, 0).toString());
            String nom=tabla1.getValueAt(fila, 1).toString();
            String cant=tabla1.getValueAt(fila, 2).toString();
            String uni_med=tabla1.getValueAt(fila, 3).toString();
            String valor_uni=tabla1.getValueAt(fila, 4).toString();
            String valor_total=tabla1.getValueAt(fila, 5).toString();
            txtNombres.setText(nom);
            txtCantidad.setText(cant);
            txtUnidadmedida.setText(uni_med);
            txtValorunitario.setText(valor_uni);
            txtValortotal.setText(valor_total);
        }
    }//GEN-LAST:event_tabla1MouseClicked

    
