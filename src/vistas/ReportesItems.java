    void listar(){
        List<Items> lista=dao.listar();
        modelo=(DefaultTableModel)tabla1.getModel();
        Object[] ob=new Object[6];
        for (int i = 0; i < lista.size(); i++) {
            ob[0]=lista.get(i).getId();
            ob[1]=lista.get(i).getNombre();
            ob[2]=lista.get(i).getCantidad();
            ob[3]=lista.get(i).getUnidad_medida();
            ob[4]=lista.get(i).getValor_unitario();
            ob[5]=lista.get(i).getValor_total();
            modelo.addRow(ob);
        }
        tabla1.setModel(modelo);
    }
    
    void agregar(){
        String nom=txtNombres.getText();
        String cantidad=txtCantidad.getText();
        String uni_medida=txtUnidadmedida.getText();
        String valor_uni=txtValorunitario.getText();
        String valor_total=txtValortotal.getText();
        Object[] ob=new Object[5];
        ob[0]=nom;
        ob[1]=cantidad;
        ob[2]=uni_medida;
        ob[3]=valor_uni;
        ob[4]=valor_total;
        dao.add(ob);
    }
    
    void actualizar(){
        int fila=tabla1.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        }else{
            String nom=txtNombres.getText();
            String cantidad=txtCantidad.getText();
            String uni_medida=txtUnidadmedida.getText();
            String valor_uni=txtValorunitario.getText();
            String valor_total=txtValortotal.getText();
            Object[] obJ=new Object[5];
            obJ[0]=nom;
            obJ[1]=cantidad;
            obJ[2]=uni_medida;
            obJ[3]=valor_uni;
            obJ[4]=valor_total;
            dao.actualizar(obJ);
        }
    }
    
    void eliminar(){
        int fila=tabla1.getSelectedRow(); 
        if(fila == -1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        }
        else{
            int ids=Integer.parseInt(tabla1.getValueAt(fila, 0).toString());
            dao.eliminar(ids);
        }
    }
