    void listar(){
        List<Facturas> lista=dao.listar();
        modelo=(DefaultTableModel)tabla1.getModel();
        Object[] ob=new Object[6];
        for (int i = 0; i < lista.size(); i++) {
            ob[0]=lista.get(i).getId();
            ob[1]=lista.get(i).getId_item();
            ob[2]=lista.get(i).getFecha();
            ob[3]=lista.get(i).getTotal_bienes();
            ob[4]=lista.get(i).getVslor_totsl();
            ob[5]=lista.get(i).getNum_entrada();
            modelo.addRow(ob);
        }
        tabla1.setModel(modelo);
    }
