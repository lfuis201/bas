    private void tabla1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla1MouseClicked
        int fila=tabla1.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        }else{
            id = Integer.parseInt(tabla1.getValueAt(fila, 0).toString());
            String id_item=tabla1.getValueAt(fila, 1).toString();
            String fecha=tabla1.getValueAt(fila, 2).toString();
            String proveedor=tabla1.getValueAt(fila, 3).toString();
            String totalbienes=tabla1.getValueAt(fila, 4).toString();
            String valor_total=tabla1.getValueAt(fila, 5).toString();
            String num_entrada=tabla1.getValueAt(fila, 6).toString();
            txtIditem.setText(id_item);
            txtFecha.setText(fecha);
            txtProveedor.setText(proveedor);
            txtTotalbienes.setText(totalbienes);
            txtValortotal.setText(valor_total);
            txtNumentrada.setText(num_entrada);
        }
    }//GEN-LAST:event_tabla1MouseClicked

